													O que é Astigmatismo?

												</h2>

												<p class="text-content">

														O olho com astigmatismo tem uma visão imperfeita, tanto para perto quanto para longe. Não tem a percepção nítida dos contrastes entre as linhas horizontais, verticais e oblíquas. O astigmatismo pode associar-se com outras anomalias visuais como a miopia, a hipermetropia ou a presbiopia (vista cansada).A córnea normalmente é arredondada enquanto no olho com astigmatismo, é ovalada. Sendo assim, os raios de luz não chegam ao mesmo ponto na retina. Tal como ocorre com os demais erros de refração, o astigmatismo pode estar relacionado a uma herança genética, porém a causa ainda é desconhecida.

